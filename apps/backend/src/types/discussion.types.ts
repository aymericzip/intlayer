import type { RenameId } from '@utils/mongoDB/types';
import type { Document, Model, ObjectIdToString, Types } from 'mongoose';

export interface Message {
  role: 'user' | 'assistant' | 'system';
  content: string;
  relatedFiles?: string[];
  timestamp: Date;
}

export type DiscussionType = 'doc' | 'dashboard';

export interface TokenUsage {
  /** Tokens consumed by the input/prompt. */
  inputTokens: number;
  /** Tokens produced in the model output. */
  outputTokens: number;
  /** Sum of inputTokens and outputTokens. */
  totalTokens: number;
}

export interface Discussion extends Document {
  id: Types.ObjectId; // DB id
  discussionId: string; // ID generated by the client for each discussion before it gets saved in the DB
  messages: Message[];
  userId?: Types.ObjectId;
  projectId?: Types.ObjectId;
  organizationId?: Types.ObjectId;
  title?: string;
  type?: DiscussionType;
  isArchived: boolean;
  /** AI model identifier used for the last completion in this discussion. */
  aiModel?: string;
  /** Token consumption recorded for the last AI call in this discussion. */
  tokenUsage?: TokenUsage;
  createdAt: Date;
  updatedAt: Date;
}

export type DiscussionAPI = ObjectIdToString<
  Discussion & {
    numberOfMessages: number;
  }
>;

export type DiscussionSchema = RenameId<Discussion>;
export type DiscussionModelType = Model<Discussion>;
export type DiscussionDocument = Document<unknown, {}, Discussion> & Discussion;
