import path from 'node:path';
import { generateSW } from 'workbox-build';

async function buildSW() {
  console.log('🏗️  Generating Service Worker for TanStack Start...');

  // TanStack Start (via Vinxi/Nitro) typically outputs client assets to .output/public
  // Adjust this variable if you have configured a custom build output directory.
  const buildDir = '.output/public';

  const { count, size } = await generateSW({
    // Output the service worker directly to the built public directory
    swDest: path.join(process.cwd(), `${buildDir}/sw.js`),

    // Precache (Static Assets) from the build output directory
    globDirectory: buildDir,

    // Precache JS, CSS, and media generated by the Vite build.
    // TanStack Start handles routing dynamically, so we focus on static assets here.
    globPatterns: ['**/*.{js,css,html,ico,png,svg,webp,woff,woff2}'],

    // Ignore the service worker itself, Workbox files, and maps
    globIgnores: ['sw.js', 'workbox-*.js', '**/node_modules/**/*', '**/*.map'],

    // Note: 'modifyURLPrefix' is removed.
    // Unlike Next.js (which uses /_next/static/), Vite/Nitro typically serves
    // assets relative to the root or an /assets/ folder inside the build dir,
    // which aligns perfectly with the glob directory structure out of the box.

    // Runtime Caching
    runtimeCaching: [
      // Cache Google Fonts & External Fonts (StaleWhileRevalidate)
      {
        urlPattern: /^https:\/\/fonts\.(?:googleapis|gstatic)\.com\/.*/i,
        handler: 'StaleWhileRevalidate',
        options: {
          cacheName: 'google-fonts',
          expiration: { maxEntries: 4, maxAgeSeconds: 365 * 24 * 60 * 60 },
        },
      },
      // Cache External/Dynamic Images (CacheFirst)
      {
        urlPattern: /\.(?:png|jpg|jpeg|svg|gif|webp|ico)$/i,
        handler: 'CacheFirst',
        options: {
          cacheName: 'images',
          expiration: { maxEntries: 50, maxAgeSeconds: 30 * 24 * 60 * 60 },
        },
      },
      // Cache JS/CSS not caught in precache (StaleWhileRevalidate)
      {
        urlPattern: /\.(?:js|css)$/i,
        handler: 'StaleWhileRevalidate',
        options: {
          cacheName: 'static-resources',
          expiration: { maxEntries: 32, maxAgeSeconds: 24 * 60 * 60 },
        },
      },
      // Cache Navigation (HTML)
      // TanStack Start handles initial document requests via the server, then JS takes over.
      {
        urlPattern: ({ request }) => request.mode === 'navigate',
        handler: 'NetworkFirst',
        options: {
          cacheName: 'pages',
          networkTimeoutSeconds: 3, // Fallback to cache quickly if offline
          expiration: { maxEntries: 20 },
        },
      },
    ],

    // Cleanup & Activation
    skipWaiting: true,
    clientsClaim: true,
    cleanupOutdatedCaches: true,
    maximumFileSizeToCacheInBytes: 5 * 1024 * 1024, // 5MB limit per file
  });

  console.log(
    `✅ Service Worker generated! Precaching ${count} files (${size} bytes)`
  );
}

buildSW();
