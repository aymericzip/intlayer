'use client';

import type { Dictionary } from '@intlayer/types/dictionary';
import type {
  DictionaryKeys,
  LocalesValues,
} from '@intlayer/types/module_augmentation';
import { useContext } from 'react';
import {
  IntlayerClientContext,
  useDictionary as useDictionaryBase,
} from 'react-intlayer';
import { createDictionaryTranslator } from './shared/namespaceTranslator';
import type {
  ScopedTranslateFunction,
  TranslateFunction,
} from './shared/translateFunctionTypes';

/**
 * Overload set for {@link useDictionary}: without a prefix the translator is
 * typed against the dictionary's dot-paths; with a prefix the keys are
 * relative dot-paths under that scope.
 */
type UseDictionary = {
  <T extends Dictionary>(
    dictionary: T
  ): TranslateFunction<T['key'] & DictionaryKeys>;
  <T extends Dictionary, Prefix extends string>(
    dictionary: T,
    namespacePrefix: Prefix
  ): ScopedTranslateFunction<T['key'] & DictionaryKeys, Prefix>;
};

/**
 * Dictionary-accepting variant of `useTranslations`.
 *
 * Used internally by the build-time optimization: instead of looking up the
 * dictionary at runtime by key, the babel/swc plugin pre-imports the dictionary
 * JSON at build time and passes it directly here. This enables tree-shaking of
 * unused locale content.
 *
 * The returned translator matches `useTranslations` exactly (ICU messages,
 * `has`, `raw`, `rich`, `markup`).
 *
 * @example (generated by the plugin, not written manually)
 * import _abc from '.intlayer/dictionaries/about.json' with { type: 'json' };
 * const t = useDictionary(_abc);
 */
export const useDictionary = (<T extends Dictionary>(
  dictionary: T,
  namespacePrefix?: string
) => {
  const { locale } = useContext(IntlayerClientContext) ?? {};
  const content = useDictionaryBase(dictionary);

  return createDictionaryTranslator(
    locale as LocalesValues,
    content,
    namespacePrefix
  );
}) as UseDictionary;
