'use client';

import type { ValidDotPathsFor } from '@intlayer/core/transpiler';
import type { Dictionary } from '@intlayer/types/dictionary';
import { useDictionary as useDictionaryBase } from 'react-intlayer';
import { navigatePath } from './shared/namespaceTranslator';

/**
 * Dictionary-accepting variant of `useTranslations`.
 *
 * Used internally by the build-time optimization: instead of looking up the
 * dictionary at runtime by key, the babel/swc plugin pre-imports the dictionary
 * JSON at build time and passes it directly here. This enables tree-shaking of
 * unused locale content.
 *
 * @example (generated by the plugin, not written manually)
 * import _abc from '.intlayer/dictionaries/about.json' with { type: 'json' };
 * const t = useDictionary(_abc);
 */
export const useDictionary = <T extends Dictionary>(
  dictionary: T,
  namespacePrefix?: string
) => {
  const content = useDictionaryBase(dictionary);

  const resolveKey = (key: string): string =>
    namespacePrefix ? `${namespacePrefix}.${key}` : key;

  return Object.assign(
    <P extends ValidDotPathsFor<string>>(
      lookup: P,
      params?: Record<string, unknown>
    ): string => {
      const rawValue = navigatePath(content, resolveKey(String(lookup)));
      const text = String(rawValue ?? resolveKey(String(lookup)));
      if (!params) return text;
      return text.replace(/\{(\w+)\}/g, (_match, key) =>
        params[key] != null ? String(params[key]) : `{${key}}`
      );
    },
    {
      has: <P extends ValidDotPathsFor<string>>(lookup: P): boolean =>
        navigatePath(content, resolveKey(String(lookup))) != null,
      raw: <P extends ValidDotPathsFor<string>>(lookup: P): unknown =>
        navigatePath(content, resolveKey(String(lookup))),
    }
  );
};
