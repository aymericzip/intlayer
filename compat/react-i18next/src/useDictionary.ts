'use client';

import type { ValidDotPathsFor } from '@intlayer/core/transpiler';
import type { Dictionary } from '@intlayer/types/dictionary';
import type { LocalesValues } from '@intlayer/types/module_augmentation';
import { useMemo } from 'react';
import type { UseTranslationOptions } from 'react-i18next';
import { useDictionary as useDictionaryBase, useLocale } from 'react-intlayer';

const navigatePath = (objectValue: unknown, path: string): unknown => {
  if (!path) return objectValue;
  let current: unknown = objectValue;
  for (const part of path.split('.')) {
    if (
      current === null ||
      current === undefined ||
      typeof current !== 'object'
    ) {
      return undefined;
    }
    current = (current as Record<string, unknown>)[part];
  }
  return current;
};

/**
 * Dictionary-accepting variant of `useTranslation`.
 *
 * Used internally by the SWC optimization: instead of looking up the dictionary
 * at runtime by key, the SWC plugin pre-imports the dictionary JSON at build time
 * and passes it directly here. This enables tree-shaking of unused locale content.
 *
 * For a nested namespace (`useTranslation('about.counter')`), the SWC passes the
 * key prefix (`'counter'`) as the second argument.
 *
 * @example (generated by SWC, not written manually)
 * import _abc from '.intlayer/dictionaries/about.json' with { type: 'json' };
 * const { t } = useDictionary(_abc);
 */
export const useDictionary = <T extends Dictionary>(
  dictionary: T,
  keyPrefix?: string | UseTranslationOptions<string>,
  options?: UseTranslationOptions<string>
) => {
  const content = useDictionaryBase(dictionary);
  const { locale, setLocale, availableLocales } = useLocale();

  const i18n = useMemo(
    () => ({
      language: locale as string,
      languages: (availableLocales ?? []) as string[],
      changeLanguage: async (lng: string) => {
        setLocale(lng as LocalesValues);
      },
      isInitialized: true,
    }),
    [locale, availableLocales, setLocale]
  );

  const resolvedOptions = typeof keyPrefix === 'object' ? keyPrefix : options;
  const prefix =
    resolvedOptions?.keyPrefix ??
    (typeof keyPrefix === 'string' ? keyPrefix : undefined);

  const t = <P extends ValidDotPathsFor<any>>(
    key: P,
    params?: Record<string, unknown>
  ): string => {
    const lookupKey = prefix ? `${prefix}.${String(key)}` : String(key);
    const rawValue = navigatePath(content, lookupKey);
    const str = String(rawValue ?? lookupKey);

    if (!params) return str;

    return str.replace(/\{\{(\w+)\}\}/g, (_, name) =>
      params[name] != null ? String(params[name]) : `{{${name}}}`
    );
  };

  return { t, i18n, ready: true };
};
