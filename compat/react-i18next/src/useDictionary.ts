'use client';

import type { ValidDotPathsFor } from '@intlayer/core/transpiler';
import type { Dictionary } from '@intlayer/types/dictionary';
import type { LocalesValues } from '@intlayer/types/module_augmentation';
import type { TOptions } from 'i18next';
import { useMemo } from 'react';
import type { UseTranslationOptions } from 'react-i18next';
import { useDictionary as useDictionaryBase, useLocale } from 'react-intlayer';
import { createTranslationApi } from './createTranslationApi';

/**
 * Dictionary-accepting variant of `useTranslation`.
 *
 * Used internally by the build-time optimization: instead of looking up the
 * dictionary at runtime by key, the babel/swc plugin pre-imports the dictionary
 * JSON at build time and passes it directly here. This enables tree-shaking of
 * unused locale content.
 *
 * For a nested namespace (`useTranslation('about.counter')`), the plugin passes
 * the key prefix (`'counter'`) as the second argument.
 *
 * The returned `{ t, i18n }` matches `useTranslation` exactly (plural and
 * context suffixes, `$t()` nesting, `defaultValue`, interpolation).
 *
 * @example (generated by the plugin, not written manually)
 * import _abc from '.intlayer/dictionaries/about.json' with { type: 'json' };
 * const { t } = useDictionary(_abc);
 */
export const useDictionary = <T extends Dictionary>(
  dictionary: T,
  keyPrefix?: string | UseTranslationOptions<string>,
  options?: UseTranslationOptions<string>
) => {
  const content = useDictionaryBase(dictionary);
  const { locale, setLocale, availableLocales } = useLocale();

  const resolvedOptions = typeof keyPrefix === 'object' ? keyPrefix : options;
  const prefix =
    resolvedOptions?.keyPrefix ??
    (typeof keyPrefix === 'string' ? keyPrefix : undefined);

  const { translate, i18n } = useMemo(
    () =>
      createTranslationApi({
        locale: locale as LocalesValues,
        setLocale: setLocale as (newLocale: LocalesValues) => void,
        availableLocales: (availableLocales ?? []) as LocalesValues[],
        namespace: dictionary.key,
        keyPrefix: prefix,
        dictionaryContent: content,
      }),
    [locale, setLocale, availableLocales, dictionary.key, prefix, content]
  );

  /** Typed facade over the untyped runtime translate function. */
  const t = translate as <P extends ValidDotPathsFor<T['key']>>(
    key: P | P[],
    optionsOrDefaultValue?: TOptions | string,
    extraOptions?: TOptions
  ) => string;

  return { t, i18n, ready: true };
};
