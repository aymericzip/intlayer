import { NextIntlClientProvider } from '@intlayer/next-intl';
import { getLocale } from '@intlayer/next-intl/server';
import type { Metadata } from 'next';
import { Geist, Geist_Mono } from 'next/font/google';
import type { ReactNode } from 'react';
import { isRTL, type Locale, locales } from '@/i18n';

const geistSans = Geist({
  variable: '--font-geist-sans',
  subsets: ['latin'],
});

const geistMono = Geist_Mono({
  variable: '--font-geist-mono',
  subsets: ['latin'],
});

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

export function generateStaticParams() {
  return locales.map((locale) => ({ locale }));
}

export default async function LocaleLayout({
  children,
  params,
}: {
  children: ReactNode;
  params: Promise<{ locale: string }>;
}) {
  const { locale } = await params;
  // getLocale() reads from the INTLAYER_LOCALE cookie / Accept-Language header.
  // We also accept the URL param as the authoritative locale for this layout.
  const resolvedLocale = locale ?? (await getLocale());

  return (
    <html lang={resolvedLocale} dir={isRTL(resolvedLocale) ? 'rtl' : 'ltr'}>
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        <NextIntlClientProvider locale={resolvedLocale as Locale}>
          {children}
        </NextIntlClientProvider>
      </body>
    </html>
  );
}
